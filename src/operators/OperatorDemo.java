package operators;

public class OperatorDemo {


    public static void main(String[] args) {
        int num1 = 10;
        System.out.println(num1++);//10
        System.out.println(num1);//11
        System.out.println(++num1);//12
        System.out.println(num1--);//12
        System.out.println(num1);//11
        System.out.println(--num1);//10

    }

}
